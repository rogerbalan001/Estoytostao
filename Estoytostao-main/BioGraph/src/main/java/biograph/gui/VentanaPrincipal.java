    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
if (grafo == null || grafo.getVertices().size() == 0) {
        JOptionPane.showMessageDialog(this, "Primero debe cargar un grafo.");
        return;
    }
    biograph.logic.Lista<biograph.logic.Lista<biograph.logic.Vertice>> complejos = grafo.obtenerComplejos();
    StringBuilder mensaje = new StringBuilder("Se encontraron " + complejos.size() + " complejos proteicos:\n\n");
    
    for (int i = 0; i < complejos.size(); i++) {
        mensaje.append("Complejo ").append(i + 1).append(": ");
        biograph.logic.Lista<biograph.logic.Vertice> comp = complejos.get(i);
        for (int j = 0; j < comp.size(); j++) {
            mensaje.append(comp.get(j).getNombre()).append(j < comp.size() - 1 ? ", " : "");
        }
        mensaje.append("\n");
    }
    JOptionPane.showMessageDialog(this, mensaje.toString(), "Resultados DFS", JOptionPane.INFORMATION_MESSAGE);        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
if (grafo == null || grafo.getVertices().size() == 0) {
        JOptionPane.showMessageDialog(this, "Primero debe cargar un grafo.");
        return;
    }
    String origen = JOptionPane.showInputDialog(this, "Ingrese el nombre de la proteína de ORIGEN:");
    if (origen == null || origen.trim().isEmpty()) return;
    
    String destino = JOptionPane.showInputDialog(this, "Ingrese el nombre de la proteína de DESTINO:");
    if (destino == null || destino.trim().isEmpty()) return;

    biograph.logic.Lista<biograph.logic.Vertice> ruta = grafo.dijkstra(origen.trim(), destino.trim());
    
    if (ruta == null) {
        JOptionPane.showMessageDialog(this, "Uno o ambos vértices no existen en el grafo.", "Error", JOptionPane.ERROR_MESSAGE);
    } else if (ruta.size() == 0) {
        JOptionPane.showMessageDialog(this, "No existe una ruta posible entre " + origen + " y " + destino, "Ruta no encontrada", JOptionPane.WARNING_MESSAGE);
    } else {
        StringBuilder mensaje = new StringBuilder("Ruta más corta desde " + origen + " hasta " + destino + ":\n");
        // Leemos la ruta de atrás hacia adelante porque Dijkstra la construye desde el destino
        for (int i = ruta.size() - 1; i >= 0; i--) {
            mensaje.append(ruta.get(i).getNombre());
            if (i > 0) mensaje.append(" -> ");
        }
        JOptionPane.showMessageDialog(this, mensaje.toString(), "Resultado de Dijkstra", JOptionPane.INFORMATION_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
if (grafo == null) {
        // Si no hay grafo, creamos uno nuevo vacío para empezar a agregar
        grafo = new biograph.logic.Grafo(); 
    }

    String origen = JOptionPane.showInputDialog(this, "Ingrese el nombre de la proteína de ORIGEN:");
    if (origen == null || origen.trim().isEmpty()) return;

    String destino = JOptionPane.showInputDialog(this, "Ingrese el nombre de la proteína de DESTINO:");
    if (destino == null || destino.trim().isEmpty()) return;

    String pesoStr = JOptionPane.showInputDialog(this, "Ingrese el costo de la interacción (número):");
    if (pesoStr == null || pesoStr.trim().isEmpty()) return;

    try {
        int peso = Integer.parseInt(pesoStr.trim());
        
        // Insertamos la arista (esto crea los vértices automáticamente si no existen)
        grafo.insertarArista(origen.trim(), destino.trim(), peso);
        
        // Actualizamos la vista gráfica (CAMBIA 'jPanel2' POR EL NOMBRE DE TU PANEL)
        ((biograph.gui.PanelGrafo) jPanel2).actualizarGrafo(grafo); 
        
        JOptionPane.showMessageDialog(this, "Conexión agregada exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "El costo (peso) debe ser un número entero válido.", "Error", JOptionPane.ERROR_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
   if (grafo == null || grafo.getVertices().size() == 0) {
        JOptionPane.showMessageDialog(this, "No hay ningún grafo en memoria para modificar.", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String nombre = JOptionPane.showInputDialog(this, "Ingrese el nombre de la proteína que desea ELIMINAR:");
    if (nombre != null && !nombre.trim().isEmpty()) {
        
        // Verificamos si existe antes de intentar eliminar
        biograph.logic.Vertice v = grafo.buscarVertice(nombre.trim());
        
        if (v != null) {
            grafo.eliminarVertice(nombre.trim());
            
            // Actualizamos la vista gráfica (CAMBIA 'jPanel2' POR EL NOMBRE DE TU PANEL)
            ((biograph.gui.PanelGrafo) jPanel2).actualizarGrafo(grafo); 
            
            JOptionPane.showMessageDialog(this, "Proteína '" + nombre + "' y todas sus conexiones han sido eliminadas.", "Eliminado", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "La proteína '" + nombre + "' no existe en el grafo.", "No encontrada", JOptionPane.ERROR_MESSAGE);
        }
    }     // TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed

