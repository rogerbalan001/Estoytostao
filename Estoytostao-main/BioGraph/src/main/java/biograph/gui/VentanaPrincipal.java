    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// 1. Advertencia requerida por el profesor (Tolerancia a fallos / Usabilidad)
    if (grafo != null && grafo.getVertices().size() > 0) {
        int resp = JOptionPane.showConfirmDialog(this, 
            "Ya hay un grafo en memoria. ¿Desea continuar y sobrescribir los datos actuales no guardados?", 
            "Advertencia de Sobrescritura", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (resp == JOptionPane.NO_OPTION) {
            return; // Cancela la acción de cargar
        }
    }

    // 2. Abrir el buscador de archivos
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Seleccione el archivo maestro CSV");
    
    int seleccion = fileChooser.showOpenDialog(this);
    if (seleccion == JFileChooser.APPROVE_OPTION) {
        java.io.File archivo = fileChooser.getSelectedFile();
        
        // 3. Reiniciar el grafo y cargar los datos
        grafo = new biograph.logic.Grafo(); 
        manejador.cargarArchivo(archivo, grafo);
        
        // 4. Actualizar la vista gráfica (CAMBIA 'jPanel2' POR EL NOMBRE DE TU PANEL)
        ((biograph.gui.PanelGrafo) jPanel2).actualizarGrafo(grafo); 
    }    }//GEN-LAST:event_jButton1ActionPerformed

